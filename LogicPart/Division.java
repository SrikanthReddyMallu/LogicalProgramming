package LogicPart;

public class Division
{
    public static void main(String[] args)
    {
        int n=8567;
        System.out.println(n);
        n=n/10;  //856
        System.out.println(n);
        n=n/10;   //85
        System.out.println(n);
        n=n/10;   //8
        System.out.println(n);
        n=n/10;   //0
        System.out.println(n);
    }
}
