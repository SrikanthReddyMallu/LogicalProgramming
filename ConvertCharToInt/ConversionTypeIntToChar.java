package ConvertCharToInt;

public class ConversionTypeIntToChar
{
    public static void main(String[] args)
    {
        //conver int to char
        int a=99;
        System.out.println(a);
        char b=(char)a;
        System.out.println(b);


        //convert small "a" chat to int
        char c='a';
        System.out.println(c);
        int d=(int) c;
        System.out.println(d);


        //convert Big "A" char to int
        char e='A';
        System.out.println(c);
        int f=(int) e;
        System.out.println(f);


        //convert int to chat
        int g=49;
        System.out.println(g);
        char h=(char) g;
        System.out.println(h);

        int i=20;
        char j='A';
        int k=(int) j+i;
        System.out.println(k);
    }
}
